const categoryById = new Map(MACRO_CATEGORIES.map(category => [category.id, category]));

function normalize(value) {
  return String(value ?? '').trim().toLowerCase().replace(/\s+/g, '');
}

function includesNormalized(value, query) {
  return normalize(value).includes(normalize(query));
}

function positiveInt(value, fallback) {
  const numeric = Number(value);
  return Number.isFinite(numeric) && numeric > 0 ? Math.floor(numeric) : fallback;
}

function categoryMatches(source, categoryQuery) {
  if (!categoryQuery) return true;
  const category = categoryById.get(source.category);
  return [source.category, category?.name, category?.section]
    .filter(Boolean)
    .some(value => includesNormalized(value, categoryQuery));
}

function sourceHaystack(source) {
  const category = categoryById.get(source.category);
  return [
    source.id,
    source.name,
    ...(source.aliases ?? []),
    category?.name,
    source.summary,
    source.frequency,
    source.credit,
    source.notes,
    source.urls.join(' '),
    source.dataTypes.join(' '),
  ].filter(Boolean).join(' ');
}

function sourceSelectorFields(source) {
  return [
    source.id,
    source.name,
    ...(source.aliases ?? []),
  ].filter(Boolean);
}

export function categoryName(categoryId) {
  return categoryById.get(categoryId)?.name ?? categoryId;
}

export function compactSource(source) {
  return {
    id: source.id,
    category: categoryName(source.category),
    name: source.name,
    frequency: source.frequency || '',
    url: source.urls.join('\n'),
    notes: source.notes || '',
  };
}

export function sourceDetailRows(source) {
  return [
    { field: 'id', value: source.id },
    { field: 'name', value: source.name },
    { field: 'category', value: categoryName(source.category) },
    { field: 'summary', value: source.summary },
    { field: 'dataTypes', value: source.dataTypes.join('、') },
    { field: 'frequency', value: source.frequency || '' },
    { field: 'urls', value: source.urls.join('\n') },
    { field: 'credit', value: source.credit || '' },
    { field: 'notes', value: source.notes || '' },
    { field: 'document', value: source.document },
  ].filter(row => row.value !== '');
}

export function listMacroSources(options = {}) {
  const limit = positiveInt(options.limit, MACRO_SOURCES.length);
  return MACRO_SOURCES
    .filter(source => categoryMatches(source, options.category))
    .slice(0, limit)
    .map(compactSource);
}

export function searchMacroSources(query, options = {}) {
  const needle = normalize(query);
  if (!needle) return listMacroSources(options);

  const limit = positiveInt(options.limit, MACRO_SOURCES.length);
  return MACRO_SOURCES
    .filter(source => categoryMatches(source, options.category))
    .filter(source => normalize(sourceHaystack(source)).includes(needle))
    .slice(0, limit)
    .map(compactSource);
}

export function findMacroSource(selector) {
  const needle = normalize(selector);
  if (!needle) return undefined;

  const exact = MACRO_SOURCES.find(source =>
    sourceSelectorFields(source).some(value => normalize(value) === needle),
  );
  if (exact) return exact;

  if (!/^[a-z0-9-]{2,}$/i.test(needle)) return undefined;

  const idPrefixMatches = MACRO_SOURCES.filter(source => normalize(source.id).startsWith(needle));
  return idPrefixMatches.length === 1 ? idPrefixMatches[0] : undefined;
}

export function listMacroSourceUrls(source) {
  return (source.urls ?? []).filter(url => /^https?:\/\//i.test(url));
}

export function resolveMacroSourceUrl(source, urlIndex = 1) {
  const urls = listMacroSourceUrls(source);
  if (!urls.length) return undefined;

  const indexValue = Number(urlIndex);
  const index = Number.isFinite(indexValue) ? Math.floor(indexValue) : 1;
  if (index < 1 || index > urls.length) return undefined;

  return {
    url: urls[index - 1],
    index,
    total: urls.length,
  };
}

export function listMacroCategories() {
  return MACRO_CATEGORIES.map(category => ({
    id: category.id,
    name: category.name,
    part: category.part,
    sources: MACRO_SOURCES.filter(source => source.category === category.id).length,
    summary: category.summary,
  }));
}

export function getMacroArticle(section) {
  const needle = normalize(section);
  const sections = !needle || needle === 'all' || needle === 'full'
    ? MACRO_ARTICLE_SECTIONS
    : MACRO_ARTICLE_SECTIONS.filter(item => {
        return normalize(item.id) === needle
          || includesNormalized(item.title, section)
          || (item.aliases ?? []).some(alias => includesNormalized(alias, section));
      });

  if (!sections.length) return undefined;

  return sections
    .map(item => `# ${item.title}\n\n${item.body}`)
    .join('\n\n');
}
